import json
import sys

from lf_version_util import parse_version_dict, version_sort_key, version_to_key

# --- Generators

def generate_haskell_list(name, docstring, version_dicts, lookup_map):
    """Generates a Haskell list definition with deduplication."""
    raw_refs = [lookup_map[version_to_key(v)] for v in version_dicts]

    unique_refs = []
    seen = set()
    for ref in raw_refs:
        if ref not in seen:
            unique_refs.append(ref)
            seen.add(ref)

    return [
        f"-- | {docstring}",
        f"{name} :: [Version]",
        f"{name} = [{', '.join(unique_refs)}]\n",
    ]

def generate_haskell_singleton(name, docstring, version_dict, lookup_map):
    """Generates a Haskell singleton definition (alias)."""
    ref = lookup_map[version_to_key(version_dict)]
    return [
        f"-- | {docstring}",
        f"{name} :: Version",
        f"{name} = {ref}\n",
    ]

def main(input_json_path, output_hs_path):
    with open(input_json_path, 'r') as f:
        data = json.load(f)

    output = [
        """-- | This file is generated by
-- //compiler/daml-lf-ast:generated_haskell_features_src based on
-- //daml-lf/daml-lf-versions.json. DO NOT EDIT.

module DA.Daml.LF.Ast.Version.GeneratedVersions where

import qualified Data.Map.Strict as Map
import DA.Daml.LF.Ast.Version.VersionType
"""
    ]

    version_lookup = {}
    generated_vars = set()
    staging_revisions_by_minor = {}  # minor_version -> set of revisions

    raw_versions = data.get("explicitVersions", [])
    raw_versions.sort(key=version_sort_key)

    for v in raw_versions:
        var_name, base_v, haskell_constructor, rev = parse_version_dict(v)

        if rev is not None:
            minor_map = v["minor"]
            minor_version = minor_map["Staging"]["version"]
            staging_revisions_by_minor.setdefault(minor_version, set()).add(rev)

        version_lookup[base_v] = var_name

        if var_name not in generated_vars:
            output.append(f"-- | Daml-LF version {base_v}")
            output.append(f"{var_name} :: Version")
            output.append(f"{var_name} = Version V2 ({haskell_constructor})\n")
            generated_vars.add(var_name)

    # Generate Map Int [Int] for acceptedStagingRevisions
    map_entries = []
    for minor in sorted(staging_revisions_by_minor.keys()):
        revs = sorted(staging_revisions_by_minor[minor])
        revs_str = ", ".join(map(str, revs))
        map_entries.append(f"({minor}, [{revs_str}])")
    map_entries_str = ", ".join(map_entries)

    output.append("-- | The acceptedStagingRevisions.")
    output.append("acceptedStagingRevisions :: Map.Map Int [Int]")
    output.append(f"acceptedStagingRevisions = Map.fromList [{map_entries_str}]\n")

    version_lists = data.get("versionLists", {})
    for list_name in sorted(version_lists.keys()):
        v_list = version_lists[list_name]
        docstring = f"The {list_name}."
        output.extend(generate_haskell_list(list_name, docstring, v_list, version_lookup))

    named_versions = data.get("namedVersions", {})
    for alias_name in sorted(named_versions.keys()):
        v_dict = named_versions[alias_name]
        docstring = f"The {alias_name}."
        output.extend(generate_haskell_singleton(alias_name, docstring, v_dict, version_lookup))

    with open(output_hs_path, 'w') as f:
        f.write("\n".join(output))

if __name__ == "__main__":
    if len(sys.argv) != 3:
        print(f"Usage: {sys.argv[0]} <input.json> <output.hs>", file=sys.stderr)
        sys.exit(1)
    main(sys.argv[1], sys.argv[2])
